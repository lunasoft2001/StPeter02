public class Tumbas {
    private long ID;
    private String COD_TUMBA;
    private String NOMBRE;
    private String CEMENTERIO;
    private String CAMPO;
    private String FILA;
    private String NUMERO;


    public long getID() {
        return ID;
    }

    public void setID(long ID) {
        this.ID = ID;
    }

    public String getCOD_TUMBA() {
        return COD_TUMBA;
    }

    public void setCOD_TUMBA(String COD_TUMBA) {
        this.COD_TUMBA = COD_TUMBA;
    }

    public String getNOMBRE() {
        return NOMBRE;
    }

    public void setNOMBRE(String NOMBRE) {
        this.NOMBRE = NOMBRE;
    }

    public String getCEMENTERIO() {
        return CEMENTERIO;
    }

    public void setCEMENTERIO(String CEMENTERIO) {
        this.CEMENTERIO = CEMENTERIO;
    }

    public String getCAMPO() {
        return CAMPO;
    }

    public void setCAMPO(String CAMPO) {
        this.CAMPO = CAMPO;
    }

    public String getFILA() {
        return FILA;
    }

    public void setFILA(String FILA) {
        this.FILA = FILA;
    }

    public String getNUMERO() {
        return NUMERO;
    }

    public void setNUMERO(String NUMERO) {
        this.NUMERO = NUMERO;
    }




}
